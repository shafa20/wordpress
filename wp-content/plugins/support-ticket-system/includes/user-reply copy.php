// Display user replies
function sts_display_user_replies($ticket_id)
{
    global $wpdb;
    $ticket_table = $wpdb->prefix . 'support_tickets';
    $replies_table = $wpdb->prefix . 'support_ticket_replies';
    $ticket = $wpdb->get_row($wpdb->prepare("SELECT * FROM $ticket_table WHERE id = %d", $ticket_id));
    $replies = $wpdb->get_results($wpdb->prepare("SELECT * FROM $replies_table WHERE ticket_id = %d", $ticket_id));

    if ($ticket) {
        echo '<h3>' . esc_html__('Ticket Replies', 'support-ticket-system') . '</h3>';
        echo '<p><strong>' . esc_html__('Subject:', 'support-ticket-system') . '</strong> ' . esc_html($ticket->subject) . '</p>';
        echo '<p><strong>' . esc_html__('Message:', 'support-ticket-system') . '</strong> ' . esc_html($ticket->message) . '</p>';

        echo '<h4>' . esc_html__('Replies', 'support-ticket-system') . '</h4>';
        if ($replies) {
            foreach ($replies as $reply) {
                $user_info = get_userdata($reply->user_id);
                echo '<p><strong>' . esc_html__('Replied by:', 'support-ticket-system') . '</strong> ' . esc_html($user_info->display_name) . ' <strong>' . esc_html__('at', 'support-ticket-system') . '</strong> ' . esc_html($reply->replied_at) . '</p>';
                echo '<p>' . esc_html($reply->message) . '</p>';
            }
        } else {
            echo '<p>' . esc_html__('No replies found.', 'support-ticket-system') . '</p>';
        }

        echo '<h4>' . esc_html__('Add Reply', 'support-ticket-system') . '</h4>';
        echo '<form method="post" action="">';
        echo '<p><textarea name="reply_message" required></textarea></p>';
        echo '<input type="hidden" name="ticket_id" value="' . esc_attr($ticket_id) . '">';
        echo '<p><input type="submit" name="sts_reply_message" value="' . esc_attr__('Submit Reply', 'support-ticket-system') . '"></p>';
        echo '</form>';
    } else {
        echo '<p>' . esc_html__('Ticket not found.', 'support-ticket-system') . '</p>';
    }
}


// Check if ticket_id is provided in query string
if (isset($_GET['ticket_id']) && is_numeric($_GET['ticket_id'])) {
    sts_display_user_replies(intval($_GET['ticket_id']));
} else {
    echo '<p>' . esc_html__('Invalid ticket ID.', 'support-ticket-system') . '</p>';
}
